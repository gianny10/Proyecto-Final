    // Metedo para poder inicar sesion en el sistema
    private void iniciarSesion() {
        if (!txtUsuario.getText().isEmpty() && !txtPass.getText().isEmpty()) {
            C_Usuario userController = new C_Usuario();
            Usuario user = new Usuario();
            user.setUsuario(txtUsuario.getText());
            user.setContrase침a(txtPass.getText());

            if (userController.loginUser(user)) {
                JOptionPane.showMessageDialog(null,"Sesi칩n iniciada");
                formMenuPrincipal menu = new formMenuPrincipal();
                menu.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null,"Usuario o ID incorrecto");
            }

        } else {
            JOptionPane.showMessageDialog(null,"Ingrese sus credenciales");
        }
    }
